#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    En,
    Zh,
}

impl Lang {
    pub fn from_str(s: &str) -> Self {
        match s {
            "zh" => Lang::Zh,
            _ => Lang::En,
        }
    }
}

