pub fn msg(key: MsgKey, lang: Lang) -> &'static str {
    match lang {
        Lang::En => en_msg(key),
        Lang::Zh => zh_msg(key),
    }
}

pub fn msg_fmt(key: MsgKey, lang: Lang, arg: &str) -> String {
    let tmpl = msg(key, lang);
    if tmpl.contains("{}") {
        tmpl.replacen("{}", arg, 1)
    } else {
        format!("{} {}", tmpl, arg)
    }
}

